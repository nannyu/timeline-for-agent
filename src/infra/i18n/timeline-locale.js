function resolveTimelineLocale(rawValue) {
  const normalized = String(rawValue || "").trim().toLowerCase();
  if (!normalized) {
    return "en";
  }
  if (normalized === "zh" || normalized === "zh-cn" || normalized.startsWith("zh-")) {
    return "zh-CN";
  }
  return "en";
}

function getTimelineText(locale, key) {
  const resolved = resolveTimelineLocale(locale);
  return TEXT[resolved]?.[key] || TEXT.en[key] || key;
}

function localizeTimelineTaxonomy(taxonomy, locale) {
  const resolved = resolveTimelineLocale(locale);
  const labels = TAXONOMY_LABELS[resolved] || TAXONOMY_LABELS.en;
  return {
    categories: Array.isArray(taxonomy?.categories)
      ? taxonomy.categories.map((category) => ({
        ...category,
        label: labels[category.id] || category.label,
        children: Array.isArray(category.children)
          ? category.children.map((child) => ({
            ...child,
            label: labels[child.id] || child.label,
          }))
          : [],
      }))
      : [],
    eventNodes: Array.isArray(taxonomy?.eventNodes)
      ? taxonomy.eventNodes.map((node) => ({ ...node }))
      : [],
  };
}

